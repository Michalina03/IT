class Math:

    @staticmethod
    def add(a, b):
        print(a + b)

    @staticmethod
    def subtraction(a, b):
        print(a - b)

    @staticmethod
    def multiplication(a, b):
        print(a * b)

    @staticmethod
    def division(a, b):
        print(a / b)


Math.add(5, 6)
Math.subtraction(10, 5)
Math.multiplication(2, 3)
Math.division(10, 2)
